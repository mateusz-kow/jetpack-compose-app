fun getColorScheme(theme: AppTheme, isDark: Boolean) = when (theme) {
    AppTheme.DEFAULT -> if (isDark) defaultDarkScheme else defaultLightScheme
    AppTheme.OCEAN -> if (isDark) oceanDarkScheme else oceanLightScheme
    AppTheme.FOREST -> if (isDark) forestDarkScheme else forestLightScheme
    AppTheme.SUNSET -> if (isDark) sunsetDarkScheme else sunsetLightScheme
}

